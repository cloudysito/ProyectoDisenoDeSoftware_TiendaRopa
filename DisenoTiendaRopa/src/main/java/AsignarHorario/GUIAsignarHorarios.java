   private void configurarTablaHorario(JTable tabla, HorarioDTO horarioActual) {

        // Columnas: primera = horas, luego los días
        String[] columnas = { "Hora", "Lunes", "Martes", "Miércoles", "Jueves", "Viernes", "Sábado" };

        // Generar horas
        java.util.List<String> horas = new java.util.ArrayList<>();
        java.time.LocalTime inicio = java.time.LocalTime.of(9, 0);
        java.time.LocalTime fin = java.time.LocalTime.of(22, 0);
        while (!inicio.isAfter(fin)) {
            horas.add(inicio.toString());
            inicio = inicio.plusMinutes(30);
        }

        Object[][] datos = new Object[horas.size()][columnas.length];
        for (int i = 0; i < horas.size(); i++) {
            datos[i][0] = horas.get(i); // primera columna = hora
        }

        // Modelo de tabla
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(datos, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tabla.setModel(modelo);
        tabla.setRowHeight(25);

        // Variables temporales de selección
        final Integer[] filaInicio = { null };
        final Integer[] filaFin = { null };
        final Integer[] columnaSeleccionada = { null };

        // Mantener los rangos seleccionados para pintar
        java.util.Map<Integer, java.util.List<int[]>> rangosPintados = new java.util.HashMap<>();
        for (int c = 1; c < columnas.length; c++) {
            rangosPintados.put(c, new java.util.ArrayList<>());
        }

        // Inicializar rangosPintados con los horarios existentes en horarioActual
        for (DiaHorarioDTO dh : horarioActual.getDias()) {
            int columna = dh.getDia().getValue(); // MONDAY=1, TUESDAY=2, etc.
            int filaInicioRango = -1, filaFinRango = -1;
            for (int i = 0; i < horas.size(); i++) {
                LocalTime h = LocalTime.parse(horas.get(i));
                if (!h.isBefore(dh.getHoraEntrada()) && filaInicioRango == -1) filaInicioRango = i;
                if (!h.isAfter(dh.getHoraSalida())) filaFinRango = i;
            }
            if (filaInicioRango != -1 && filaFinRango != -1) {
                rangosPintados.get(columna).add(new int[]{filaInicioRango, filaFinRango});
            }
        }

        // Renderer
        javax.swing.table.DefaultTableCellRenderer renderer = new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {

                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                if (column == 0) {
                    setBackground(java.awt.Color.LIGHT_GRAY); // columna de horas
                } else {
                    boolean pintado = false;
                    for (int[] rango : rangosPintados.get(column)) {
                        if (row >= rango[0] && row <= rango[1]) {
                            pintado = true;
                            break;
                        }
                    }
                    setBackground(pintado ? java.awt.Color.CYAN : java.awt.Color.WHITE);
                }
                return this;
            }
        };
        tabla.setDefaultRenderer(Object.class, renderer);

        // Listener para seleccionar rango de horas
        tabla.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int fila = tabla.getSelectedRow();
                int columna = tabla.getSelectedColumn();

                if (fila == -1 || columna <= 0) return; // ignorar columna de horas

                if (columnaSeleccionada[0] == null) {
                    columnaSeleccionada[0] = columna;
                    filaInicio[0] = fila;
                    filaFin[0] = null;
                    return;
                }

                if (columna != columnaSeleccionada[0]) {
                    columnaSeleccionada[0] = columna;
                    filaInicio[0] = fila;
                    filaFin[0] = null;
                    return;
                }

                filaFin[0] = fila;

                int start = Math.min(filaInicio[0], filaFin[0]);
                int end = Math.max(filaInicio[0], filaFin[0]);

                // Crear DiaHorarioDTO
                DayOfWeek dia = DayOfWeek.of(columna); // columna 1 = lunes
                LocalTime horaEntrada = LocalTime.parse((String) tabla.getValueAt(start, 0));
                LocalTime horaSalida = LocalTime.parse((String) tabla.getValueAt(end, 0));

                DiaHorarioDTO dh = new DiaHorarioDTO();
                dh.setDia(dia);
                dh.setHoraEntrada(horaEntrada);
                dh.setHoraSalida(horaSalida);

                // Agregar al Horario actual
                horarioActual.getDias().add(dh);

                // Guardar el rango para pintar permanentemente
                rangosPintados.get(columna).add(new int[]{start, end});

                // Limpiar selección temporal
                filaInicio[0] = null;
                filaFin[0] = null;
                columnaSeleccionada[0] = null;

                // Repintar tabla
                tabla.repaint();

                System.out.println("Horario agregado: " + dia + " " + horaEntrada + " - " + horaSalida);
            }
        });

        // Repintar inicialmente para mostrar horarios existentes
        tabla.repaint();
    }



